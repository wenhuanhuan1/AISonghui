import Foundation
import UIKit
/// 字体

func pingfangSemibold(size: CGFloat) -> UIFont? {
    return UIFont(name: "PingFangSC-Semibold", size: size)
}

func pingfangRegular(size: CGFloat) -> UIFont? {
    return UIFont(name: "PingFang-SC-Regular", size: size)
}

func pingfangLight(size: CGFloat) -> UIFont? {
    return UIFont(name: "PingFangSC-Light", size: size)
}

func pingfangMedium(size: CGFloat) -> UIFont? {
    return UIFont(name: "PingFangSC-Medium", size: size)
}
